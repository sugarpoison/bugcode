# addition
print(2 + 3)
print(2.0 + 3.0)

# subtraction 
print(2. - 3.0)
print(2 - 3)

# multiplication
print(2 * 3)

# division
print(2 / 3)

# integer division 
print(2.0 // 3)
print(-6.0 // -3)

# exponentation
print(2 ** 3)

# exponentation using floating point numbers
print(2 ** 3.0)

# modulus
print(2 % 3)
